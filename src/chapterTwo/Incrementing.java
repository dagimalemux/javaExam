package chapterTwo;

public class Incrementing {
	public static void main(String[] args) {
		int c = 5;
	    int d = 5;
		
		System.out.println(c);
		System.out.println(c++);
		System.out.println(c);
		System.out.println("--------------------------------");
		
		System.out.println(d);
		System.out.println(++d);
		System.out.println(d);
	}

}
